import numpy as np
from PIL import Image


def enhance_and_resize_image(input_matrix, output_file):
    """
    将输入的128x128灰度图像矩阵（一维表示）转换为240x240分辨率，并保存到文件。
    输出图像以一维矩阵形式保存，且每个值以十六进制格式存储（带0x前缀）。
    输出文件将分行保存，每行对应图像的一行，每行末尾加上逗号。

    参数：
        input_matrix (list): 128x128的灰度图像矩阵（一维表示）
        output_file (str): 输出文件的路径
    """
    # 输入矩阵为一维，先将其转换为128x128的二维矩阵
    original_size = 128
    input_matrix_2d = np.array(input_matrix, dtype=np.uint8).reshape((original_size, original_size))

    # 将二维矩阵转换为灰度图像
    image = Image.fromarray(input_matrix_2d, mode='L')

    # 调整图像大小到240x240，使用LANCZOS算法
    new_size = 240
    resized_image = image.resize((new_size, new_size), Image.LANCZOS)

    # 将调整后的图像转换回二维矩阵
    output_matrix_2d = np.array(resized_image)

    # 保存到文件（以文本形式保存二维矩阵，每行对应图像的一行）
    with open(output_file, 'w') as f:
        for row in output_matrix_2d:
            # 将每行的值转换为带0x前缀的十六进制格式，并用逗号分隔
            hex_row = ",".join(f"0x{value:02X}" for value in row) + ","  # 在每行末尾加上逗号
            f.write(hex_row + "\n")  # 换行

    print(f"处理后的图像已保存到 {output_file}")


